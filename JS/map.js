/* Open when someone clicks on the span element */
function openFeeds() {
    document.getElementById("feeds").style.width = "100%";
}

/* Close when someone clicks on the "x" symbol inside the overlay */
function closeFeeds() {
    document.getElementById("feeds").style.width = "0%";
}

